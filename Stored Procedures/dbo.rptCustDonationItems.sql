create proc [dbo].[rptCustDonationItems] as 

BEGIN 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

select 

 SEASON
,CUSTOMER
,ITEM
,I_OQTY
,I_PRICE
,I_OQTY * I_PRICE AS PLEDGE
,I_PAY PAID_TO_DATE
,I_BAL BALANCE
,I_BPTYPE  PAY_PLAN_TYPE 

	FROM TK_ODET   WHERE --SEASON = 'F14' AND 
	ITEM LIKE 'DON%' 


END


GO
